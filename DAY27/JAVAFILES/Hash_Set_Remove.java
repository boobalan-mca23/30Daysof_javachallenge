import java.util.*;
public class Hash_Set_Remove {
    public static void main(String[] args) {
        HashSet <Integer> set=new HashSet<>();

        set.add(1);
        set.add(2);
        set.add(3);
        set.add(4);
        set.add(5);
        System.out.println("ORIGINAL SET :"+" "+set);
        set.clear();
        System.out.println("REMOVED SET :"+" "+set);


    }
}
