import java.util.*;
public class Hash_Get_Size {
    public static void main(String[] args) {
        HashSet<Integer> set=new HashSet<>();
        set.add(100);
        set.add(2);
        set.add(1);
        set.add(300);
        set.add(21);
        set.add(6);

        System.out.println("the size of the Hash set is "+set.size() );
    }
}
